    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        int indLinha = tblConsultaProduto.getSelectedRow();

        if (indLinha < 0) {

            JOptionPane.showMessageDialog(null, "Para alterar um dado no sistema, é necessário clicar e alterar o dado desejado!");
        }

        TableModel tabela = tblConsultaProduto.getModel();

        int indTab = tabela.getColumnCount();
        Object[] dadosAlterar = new Object[indTab];

        for (int i = 0; i < indTab; i++) {
            dadosAlterar[i] = tabela.getValueAt(indLinha, i);
        }

        Produtos obj = new Produtos();
        String descricao = (String) dadosAlterar[0];
        float preco = Float.parseFloat((String) dadosAlterar[1]);
        String validade = (String) dadosAlterar[2];
        int quantidade = Integer.parseInt((String) dadosAlterar[3]);

        obj.setDescricao(descricao);
        obj.setPreco(preco);
        obj.setValidade(validade);
        obj.setQuantidade(quantidade);

        //descricao = txtDescricao.getText();

        boolean retorno = ProdutosDAO.atualizaEstoque(obj, descricao);

        if (retorno) {
            JOptionPane.showMessageDialog(rootPane, "Estoque atualizado");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Fracasso");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

