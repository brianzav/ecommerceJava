    private void btnBuscarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarConsultaActionPerformed
        // TODO add your handling code here:
        String produto = txtProdutoConsulta.getText();
        
        ArrayList<Produtos> lista = ProdutosDAO.listar(produto);
        
        DefaultTableModel modelo = (DefaultTableModel) tblConsultaProduto.getModel();
        modelo.setRowCount(0);
        
        for (Produtos item: lista) {
            modelo.addRow(new String[]{
                                       String.valueOf(item.getDescricao()),
                                       String.valueOf(item.getPreco()),
                                       String.valueOf(item.getValidade()),
                                       String.valueOf(item.getQuantidade())
                                        });
        }
        
        int somaEstoque = ProdutosDAO.countEstoque(produto);
        String Estoque = Integer.toString(somaEstoque);
        lblProdutoEstoque.setText(Estoque);
        
        
        float totalEstoque = ProdutosDAO.sumEstoque(produto);
        String preco = Float.toString(totalEstoque);
        lblTotalEstoque.setText(preco);

    }//GEN-LAST:event_btnBuscarConsultaActionPerformed

