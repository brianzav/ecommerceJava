    private void iconAdicionar_CarrinhoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_iconAdicionar_CarrinhoMouseClicked
        // TODO add your handling code here:

        int codigo = tblCarrinho.getRowCount();

        //Recebendo valores
        String cliente = lblNomeVendas.getText();
        
        int codigoProduto = Integer.parseInt(txtCodigoProdutoVendas.getText());
        int quantidade = Integer.parseInt(spnQuantidadeVendas.getValue().toString());
        String produto = lblProdutoVendas.getText();
        float valor_prod = Float.parseFloat(lblValorProdutoVendas.getText());
        float subtotal = quantidade * valor_prod;

        DefaultTableModel tabela_carrinho = (DefaultTableModel) tblCarrinho.getModel();
        codigo = codigo + 1;
        tabela_carrinho.addRow(new Object[]{codigo, cliente, produto, quantidade, valor_prod, subtotal});

        TableModel model = tblCarrinho.getModel();

        int coluna = 5;

        float total = 0;

        for (int i = 0; i < model.getRowCount(); i++) {
            float value = Float.parseFloat(model.getValueAt(i, coluna).toString());
            total += value;

            String tt = String.valueOf(total);

            txtTotal_Compra.setText(tt);
        }


    }//GEN-LAST:event_iconAdicionar_CarrinhoMouseClicked

