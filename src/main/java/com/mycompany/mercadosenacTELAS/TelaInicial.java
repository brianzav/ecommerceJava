    private void txtCodigoProdutoVendasFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodigoProdutoVendasFocusLost
        // TODO add your handling code here:
        
       int codigoProd = Integer.parseInt(txtCodigoProdutoVendas.getText());
       
       String produto = VendasDAO.ProdutoVendas(codigoProd);
       
       lblProdutoVendas.setText(produto);
       
       
       float precoProduto = VendasDAO.precoProdutoVenda(codigoProd);
       String valorProd = String.valueOf(precoProduto);
       lblValorProdutoVendas.setText(valorProd);
       
       
    }//GEN-LAST:event_txtCodigoProdutoVendasFocusLost

