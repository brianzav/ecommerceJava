    private void btnAdicionarProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAdicionarProdutoMouseClicked
        // TODO add your handling code here:
        int desc = txtDescricao.getText().length();
        int prec = txtPreco.getText().length();
        int val = fmtValidade.getText().length();
        int qtd = txtQuantidade.getText().length();

        if (desc < 2 || prec <= 0 || val <= 0 || qtd <= 0) {
            JOptionPane.showMessageDialog(rootPane, "Para cadastrar um produto você precisa preencher todos os campos");
        }

        String descricao = txtDescricao.getText();
        float preco = Float.parseFloat(txtQuantidade.getText());
        String validade = fmtValidade.getText();
        int quantidade = Integer.parseInt(txtQuantidade.getText());

        Produtos obj = new Produtos();
        obj.setDescricao(descricao);
        obj.setPreco(preco);
        obj.setValidade(validade);
        obj.setQuantidade(quantidade);

        boolean retorno = ProdutosDAO.salvarProdutos(obj);

        if (retorno) {
            JOptionPane.showMessageDialog(rootPane, "Sucesso");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Fracasso");
        }

        txtDescricao.setText("");
        txtPreco.setText("");
        txtQuantidade.setText("");
        fmtValidade.setText("");

    }//GEN-LAST:event_btnAdicionarProdutoMouseClicked

